package day12_conditional_statements;

public class CompareChars {
    public static void main(String[] args) {
        char letter1 = 'A', letter2 = 'B';
        System.out.println(letter1==letter2);
        System.out.println(letter1>letter2);
        System.out.println(letter1<letter2);
        System.out.println();
        char grade = 'D';
        boolean pass = grade<='C';
        System.out.println(pass);


    }
}
